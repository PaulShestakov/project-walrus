const COMPANIES_TABLE = 'wikipet.COMPANIES';
const COMPANIES_TYPES_TABLE = 'wikipet.COMPANIES_TYPES';
const COMPANIES_EXACT_TYPES_TABLE = 'wikipet.COMPANIES_EXACT_TYPES';

export default {
	GET: `
		SELECT
			c.COMPANY_ID,
			c.COMPANY_TYPE_ID,
			c.COMPANY_EXACT_TYPE_ID,
			c.NAME,
			c.LOGO,
			c.DESCRIPTION,
			c.EMAIL,
			c.URL,
			c.PHONE,
			c.LOCATION,

			ct.COMPANY_TYPE_NAME,

			cet.COMPANY_EXACT_TYPE_NAME

		FROM ${COMPANIES_TABLE} AS c

		INNER JOIN ${COMPANIES_TYPES_TABLE} AS ct
			ON c.COMPANY_TYPE_ID = ct.COMPANY_TYPE_ID

		INNER JOIN ${COMPANIES_EXACT_TYPES_TABLE} AS cet
			ON c.COMPANY_TYPE_ID = cet.COMPANY_TYPE_ID
				AND c.COMPANY_EXACT_TYPE_ID = cet.COMPANY_EXACT_TYPE_ID

		WHERE c.COMPANY_ID = ?
	`,

	GET_COMPANIES_TYPES: `
		SELECT
			ct.COMPANY_TYPE_ID,
			ct.COMPANY_TYPE_NAME,

			cet.COMPANY_EXACT_TYPE_ID,
			cet.COMPANY_EXACT_TYPE_NAME

		FROM ${COMPANIES_TYPES_TABLE} AS ct

		INNER JOIN ${COMPANIES_EXACT_TYPES_TABLE} AS cet
			ON ct.COMPANY_TYPE_ID = cet.COMPANY_TYPE_ID

	`
}